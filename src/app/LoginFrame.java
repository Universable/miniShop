    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String checkName = jTextField1.getText();
        String checkPassword = String.valueOf(jPasswordField1.getPassword());
        int loggedin=0;
        for (int i = 0; i < logins.size(); i++) {
            String Name = logins.get(i).split("---")[0];
            String Password = logins.get(i).split("---")[1];
            if(checkName.equals(Name) && checkPassword.equals(Password)){
                db.deleteall("CurrentOrder");
                loggedin=1;
                NewJFrame jframe1 = new NewJFrame(Integer.parseInt(logins.get(i).split("---")[2]), 1);
                jframe1.setVisible(true);
                this.dispose();
            }
        }
        if(loggedin==0){
                JOptionPane.showMessageDialog(null, "Грешно потребителско име или парола!", "Грешка!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

